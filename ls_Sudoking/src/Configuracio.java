
public class Configuracio {
	int num;
	boolean estat;

	public Configuracio(){}

	public Configuracio(int num, boolean estat) {
		super();
		this.num = num;
		this.estat = estat;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public boolean isEstat() {
		return estat;
	}

	public void setEstat(boolean estat) {
		this.estat = estat;
	}

}
